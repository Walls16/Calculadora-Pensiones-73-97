def _guardar_cache(datos: dict) -> None:
    try:
        with open(CACHE_TABLAS_VIDA, "w", encoding="utf-8") as f:
            json.dump(datos, f, ensure_ascii=False, indent=2)
    except OSError as e:
        log.error("No se pudo guardar cache tablas de vida: %s", e)


