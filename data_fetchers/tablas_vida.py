    datos = {
        "hombre": {str(k): v for k, v in _QX_HOMBRE_EXCEL.items()},
        "mujer":  {str(k): v for k, v in _QX_MUJER_EXCEL.items()},
        "tasa_mejora_anio": 2023,
        "_meta": {"fecha_actualizacion": "2024-01-01",
                  "fuente": "Excel fuente EMSSA-09 tasa mejora 2023"},
    }
    _guardar_cache(datos)
    log.info("Cache tablas de vida inicializado con valores exactos del Excel fuente.")
